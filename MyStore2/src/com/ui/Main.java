    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
                                FileOperate filedemo=new FileOperate();
                                
                                String name=nameInput.getText().trim();
				
				String xs=XSInput.getText().trim();
				String s=SInput.getText().trim();
				String m=MInput.getText().trim();
				String l=LInput.getText().trim();
				String xl=XLInput.getText().trim();
				String color=colorInput.getText().trim();
				
				int num;
				String str="";
				
				try {
				    
					str="XS";
				    num=Integer.valueOf(xs);
				    
				    str="S";
				    num=Integer.valueOf(s);
				    
				    str="M";
				    num=Integer.valueOf(m);
				    
				    str="L";
				    num=Integer.valueOf(l);
				    
				    str="XL";
				    num=Integer.valueOf(xl);
				    
				   
				    
				   } catch (Exception e1) {
					   JOptionPane.showMessageDialog(null, str+" must be a Number","ERROR", JOptionPane.ERROR_MESSAGE);
					   return;
				   }
				
				
				
				
				String item=(String)addcombox.getSelectedItem();
				
				
				
				String content=name+"%"+xs+"%"+s+"%"+m+"%"+l+"%"+xl+"%"+color;
				
				boolean success=Sale(content,item,filedemo);
				
				if(success)
				{
					JOptionPane.showMessageDialog(null, "Operation succeeded !","Congratulations", JOptionPane.INFORMATION_MESSAGE);
				}
				
				else
				{
					JOptionPane.showMessageDialog(null, "Can not found this record !","Failed", JOptionPane.OK_OPTION);
					return;
				}
				
				//save sale records to the file
				String all=item+"%"+name+"%"+xs+"%"+s+"%"+m+"%"+l+"%"+xl+"%"+color;
				
				filedemo.WriteFile(all, "sale.txt");
        
    }//GEN-LAST:event_saveBtnActionPerformed

